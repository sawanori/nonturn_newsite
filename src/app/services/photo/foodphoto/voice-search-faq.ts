/**
 * 音声検索用の構造化データを生成
 */
export function generateVoiceSearchSchema() {
  return {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    mainEntity: voiceSearchFAQs.map(faq => ({
      '@type': 'Question',
      name: faq.question,
      acceptedAnswer: {
        '@type': 'Answer',
        text: faq.answer,
      },
      // 追加のメタデータ
      keywords: [...faq.voiceVariations, ...faq.longTailKeywords].join(', '),
    })),
  }
}

/**
 * Speakable構造化データを生成（音声アシスタント対応）
 */
export function generateSpeakableSchema() {
  return {
    '@context': 'https://schema.org',
    '@type': 'WebPage',
    name: '飲食店撮影PhotoStudio - よくある質問',
    speakable: {
      '@type': 'SpeakableSpecification',
      cssSelector: ['.faq-question', '.faq-answer'],
      xpath: [
        '//*[@class="faq-question"]',
        '//*[@class="faq-answer"]',
      ],
    },
  }
}